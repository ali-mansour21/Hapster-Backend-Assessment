    }

    /**
     * Handle the Order "updated" event.
     */
    public function updated(Order $order): void
    {
        if ($order->wasChanged('status') && in_array($order->status, ['completed', 'failed'], true)) {
            SendOrderReceiptJob::dispatch($order->id);
        }
    }

